int mindistance(int distance[],bool stat[])
{
	int minimum=INT_MAX,ind;
	for(int k=0;k<30;k++)
	{
		if(stat[k]==false && distance[k]<=minimum)
		{
			minimum=distance[k];
			ind=k;
		}
	}
	return ind;
}
void dijkstras(int g[30][30],int source,int destination, string stations[30])
{
	int distance[30];
	bool stat[30];          
	for(int k=0;k<30;k++)
	{
		distance[k]=INT_MAX;
		stat[k]=false;
	}
	distance[source]=0;
	for(int k=0;k<30;k++)
	{
		int m=mindistance(distance,stat);
		stat[m]=true;
		for(int k=0;k<30;k++)
		{
			if(!stat[k] && g[m][k] && distance[m]!=INT_MAX && distance[m]+g[m][k]<distance[k])
				distance[k]=distance[m]+g[m][k];
		}
	}
	for(int k=0;k<30;k++)
	{
		if(k==destination)
		cout<<"Minimum Stations from "<<stations[source]<<" To "<<stations[destination]<<"  are  "<<distance[k]<<endl;
	}
}

